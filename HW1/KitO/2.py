# Define variables a and b. Read values a and b from console and calculate: 
# a + b, a - b, a * b, a / b, a**b. Output obtained results.

a = 23
b = 5

print(f'a + b = {a} + {b} = {a + b}')
print(f'a - b = {a} - {b} = {a - b}')
print(f'a * b = {a} * {b} = {a * b}')
print(f'a / b = {a} / {b} = {a / b}')
print(f'a ** b = {a} ** {b} = {a ** b}')